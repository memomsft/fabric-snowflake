-- Snowflake Demo Database for Fabric Mirroring Workshop

CREATE OR REPLACE DATABASE FABRIC_MIRROR_DEMO;
USE DATABASE FABRIC_MIRROR_DEMO;
CREATE OR REPLACE SCHEMA SALES;

-- Customers table
CREATE OR REPLACE TABLE SALES.CUSTOMERS (
    CUSTOMER_ID INT AUTOINCREMENT PRIMARY KEY,
    CUSTOMER_NAME STRING,
    COUNTRY STRING
);

-- Products table
CREATE OR REPLACE TABLE SALES.PRODUCTS (
    PRODUCT_ID INT AUTOINCREMENT PRIMARY KEY,
    PRODUCT_NAME STRING,
    CATEGORY STRING,
    PRICE NUMBER(10,2)
);

-- Orders table
CREATE OR REPLACE TABLE SALES.ORDERS (
    ORDER_ID INT AUTOINCREMENT PRIMARY KEY,
    CUSTOMER_ID INT,
    PRODUCT_ID INT,
    QUANTITY INT,
    ORDER_DATE DATE,
    STATUS STRING,
    TOTAL_AMOUNT NUMBER(10,2)
);

